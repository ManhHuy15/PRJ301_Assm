public class GradeDetail {
    private int id;
    private String dur;
    private String qType;
    private int noQ;
    private String grGuide;
    private String descr;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDur() {
        return dur;
    }

    public void setDur(String dur) {
        this.dur = dur;
    }

    public String getqType() {
        return qType;
    }

    public void setqType(String qType) {
        this.qType = qType;
    }

    public int getNoQ() {
        return noQ;
    }

    public void setNoQ(int noQ) {
        this.noQ = noQ;
    }

    public String getGrGuide() {
        return grGuide;
    }

    public void setGrGuide(String grGuide) {
        this.grGuide = grGuide;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }
    
    
}
